package question15a;

public class ClassImplementsInterface implements Interface {
	
	public int addMethod(int x, int y) {
		return x+y;
	}
	public int subtractMethod(int x, int y) {
		return x-y;
	}
	public int multiplyMethod(int x, int y) {
		return x*y;
	}
	public int divideMethod(int x, int y) {
		return x/y;
	}
	
}
