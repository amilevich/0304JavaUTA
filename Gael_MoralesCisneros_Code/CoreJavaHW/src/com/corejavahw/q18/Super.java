package com.corejavahw.q18;

//abstract class with abstract methods
public abstract class Super {

	//constructor
	public Super() {}

	//abstract method to check for upperCase letters in a string
	public abstract boolean upperCaseCheck(String s);

	//abstract method to change the lowerCase letters in the string to upperCase
	public abstract String lowToUp(String s);

	//abstract method to change the string into an integer and add 10
	public abstract int stringToInt(String s);

}
