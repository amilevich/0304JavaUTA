public class Minimum {
		
	
	public static void main(String[] args) {
		
	  int minimumNumber;
	  int x = 4;
	  int y = 7;
	  
	  //ternary operator to find the minimum value
	  minimumNumber =(x < y) ? x : y;
	  
	  //prints out the minimum number
	  System.out.println("The minimum number is: " + minimumNumber);
	  
	  
	}

}
